function createEffect(area_w, area_h)
    --point1, point2, point3, point4
    local temp = 5
    local point1 = cc.p(temp, temp)
    local point2 = cc.p(0, area_h - temp)
    local point3 = cc.p(area_w - temp, area_h - temp)
    local point4 = cc.p(area_w - temp, 0)

    local spead = 1 / 124
    local durW = spead * area_w
    local durH = spead * area_h
    local node = game.newNode()
    local part1 = createParticle("ui_a003001",nil,false)--添加特效粒子效果的相对位置 
    local part2 = createParticle("ui_a003001",nil,false)--添加特效粒子效果的相对位置 
    part2:setPosition(point3)
    part1:setPosition(0, 0)

    local moveFrom1_2 =  cc.MoveTo:create(durH, point2)
    local moveFrom2_3 =  cc.MoveTo:create(durW, point3)
    local moveFrom3_4 =  cc.MoveTo:create(durH, point4)
    local moveFrom4_1 =  cc.MoveTo:create(durW, point1)
    -- local i = 1
    -- local function callFunc( )
    --     print("callFunc==",i)
    --     i = i+1
    -- end
    -- local testCallFun = cc.CallFunc:create(callFunc)

    local sequence1 = cc.Sequence:create(moveFrom1_2, moveFrom2_3, moveFrom3_4, moveFrom4_1)

    local sequence2 = cc.Sequence:create( moveFrom3_4, moveFrom4_1, moveFrom1_2, moveFrom2_3)
    local repeatForever1 = cc.RepeatForever:create(sequence1)
    local repeatForever2 = cc.RepeatForever:create(sequence2)
    part1:runAction(repeatForever1)
    part2:runAction(repeatForever2)
    node:addChild(part1)
    node:addChild(part2)
    return node
end





















